package org.example.models.generic;

public class BusinessObject
{
    private int id;
    private String name;

    public BusinessObject()
    {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
